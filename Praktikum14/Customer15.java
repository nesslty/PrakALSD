package Praktikum14;
public class Customer15 {
    public int id;
    public String name;

    public Customer15() {

    }

    public Customer15(int id, String name) {
        this.id = id;
        this.name = name;
    }

    public String toString() {
        return " ID: " + this.id + " Nama: " + this.name;
    }
}